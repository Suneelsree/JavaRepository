class AsciiValue {

    public static void main(String[] args) {

        //Decleration && intiallization
        char ch = 'a';
        int ascii = ch;
         //type casting
        int castAscii = (int) ch;
        //displaying output
        System.out.println("The ASCII value of " + ch + " is: " + ascii);
        System.out.println("The ASCII value of " + ch + " is: " + castAscii);
    }
}
